#include "libft.h"

void	ft_putnbr(int i)
{
	if (i == -2147483648)
	{
		ft_putnbr(-214748364);
		ft_putnbr(8);
		return ;
	}
	if (i < 0)
	{
		ft_putchar('-');
		i = i * (-1);
	}
	if (i < 10)
		ft_putchar(i + 48);
	else
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
