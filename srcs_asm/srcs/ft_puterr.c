#include "libasm.h"

void	ft_puterr(char *s)
{
	int i;

	i = -1;
	while (s[++i])
		write(2, &s[i], 1);
}

void	ft_putnerr(int n)
{
	char *s;

	s = ft_itoa(n);
	ft_puterr(s);
}
